def run_demo():
    log("DISTRIBUTED COMPUTATION DEMO")
    log(f"Date: 2026-03-23")
    log(f"System: Quantum Foundation — Phase 7 Full Simulation")

    # -----------------------------------------------------------------------
    section("PHASE A — Normal Operations (All Nodes in Sync)")
    # -----------------------------------------------------------------------

    hub, nodes = setup_system()
    checker = DistributedInvariantChecker(hub)
    reconciler = ReconciliationEngine(hub)

    node_a, node_b, node_c = nodes["Node_A"], nodes["Node_B"], nodes["Node_C"]

    # Operation 1: H gate from Node A
    r1 = hub.submit(node_a.propose_evolve("H"))
    log(f"Op 1 [EVOLVE H] causal_id={r1.sequenced_event.causal_id} — {r1.all_applied}")

    # Operation 2: SYNC
    r_sync1 = hub.submit(node_a.propose_sync())
    log(f"Op 2 [SYNC]     {r_sync1.sync_report.summary()}")

    # Operation 3: Measurement from Node B (seed=42)
    r3 = hub.submit(node_b.propose_measure("m1", 42))
    log(f"Op 3 [MEASURE]  causal_id={r3.sequenced_event.causal_id} — {r3.all_applied}")

    # Invariant check after Phase A
    log("\nInvariant check after Phase A:")
    inv_a = checker.run_full_check()
    print_invariant_report(inv_a)
    print_node_status(hub, "Phase A — Node Status")
    assert inv_a.clean, f"Phase A invariant failed: {inv_a.halt_reasons}"

    # -----------------------------------------------------------------------
    section("PHASE B — Controlled Divergence")
    # -----------------------------------------------------------------------

    # Op 4: X gate — delay Node B (it lags)
    r4 = hub.submit(node_a.propose_evolve("X"), delay_nodes=["Node_B"])
    log(f"Op 4 [EVOLVE X] causal_id={r4.sequenced_event.causal_id} — Node_B delayed")

    # Op 5: H gate — Node C missing this event permanently
    r5 = hub.submit(node_a.propose_evolve("H"), exclude_nodes=["Node_C"])
    log(f"Op 5 [EVOLVE H] causal_id={r5.sequenced_event.causal_id} — Node_C excluded")

    # Op 6: H gate — all receive it (Node B buffers, Node C buffers)
    r6 = hub.submit(node_a.propose_evolve("X"))
    log(f"Op 6 [EVOLVE X] causal_id={r6.sequenced_event.causal_id} — all receive")

    log("\nDivergence state:")
    print_node_status(hub, "Phase B — Divergence")
    log("\n  Is consensus broken?")
    consensus_b = print_consensus(hub)

    # Invariant check — should flag partial/diverged nodes
    log("\nInvariant check during divergence:")
    inv_b = checker.run_full_check()
    print_invariant_report(inv_b)
    assert inv_b.system_should_halt, "Expected halt signal during divergence"
    log("  ✓ Divergence correctly detected (system_should_halt=True)")

    # -----------------------------------------------------------------------
    section("PHASE C — Reconciliation")
    # -----------------------------------------------------------------------

    # First: release held events to Node B
    log("Releasing held events to Node_B...")
    hub.release_held_events("Node_B")
    log(f"  Node_B committed_id={node_b.committed_causal_id}, partial={node_b.is_partial}")

    # Now reconcile Node C (missing event 5) via event log replay
    log("\nReconciling Node_C (missing causal_id=5)...")
    recon_report = reconciler.reconcile_all()
    log(f"  {recon_report.summary()}")
    for r in recon_report.reconciled_nodes:
        log(f"  {r.node_id}: replayed={r.events_replayed}, "
            f"skipped={r.events_already_committed}, "
            f"converged={r.converged}")

    log("\nPost-reconciliation node status:")
    print_node_status(hub, "Phase C — After Reconciliation")

    # -----------------------------------------------------------------------
    section("PHASE D — Final Invariant Enforcement + Consensus Proof")
    # -----------------------------------------------------------------------

    log("Final SYNC step:")
    r_sync_final = hub.submit(node_a.propose_sync())
    log(f"  {r_sync_final.sync_report.summary()}")

    log("\nFinal invariant check:")
    inv_final = checker.run_full_check()
    print_invariant_report(inv_final)

    log("\nFinal consensus:")
    consensus_final = print_consensus(hub)

    # Hash proof
    all_hashes = {n.node_id: n.get_state_hash() for n in hub.nodes}
    log("\nFinal state hashes:")
    for nid, h in all_hashes.items():
        log(f"  {nid}: {h}")

    unique = set(all_hashes.values())
    assert len(unique) == 1, f"Hash mismatch! {all_hashes}"
    log(f"\n  ✓ All {len(all_hashes)} nodes share the same state hash.")

    # Replay hash
    replay_hash = hash_event_log(hub)
    log(f"\nEvent log hash (replay proof): {replay_hash}")
    log(f"Total events in log: {len(hub.get_event_log())}")

    # -----------------------------------------------------------------------
    section("PHASE E — Summary")
    # -----------------------------------------------------------------------

    log(f"Events processed : {hub.next_causal_id - 1}")
    log(f"Nodes reconciled : {len(recon_report.reconciled_nodes)}")
    log(f"Final consensus  : {consensus_final}")
    log(f"Invariants clean : {inv_final.clean}")
    log(f"Replay hash      : {replay_hash[:32]}...")
    log(f"State hash (all) : {list(unique)[0][:32]}...")
    log()
    log("RESULT: PASS — Deterministic distributed computation protocol verified.")

    return {
        "all_hashes": all_hashes,
        "replay_hash": replay_hash,
        "inv_final_clean": inv_final.clean,
        "consensus_final": consensus_final,
        "events_total": hub.next_causal_id - 1,
        "recon_report": recon_report,
        "log_lines": _log_lines
    }


